  code VARCHAR(12) NOT NULL,
  timeframe VARCHAR(8) NOT NULL,
  bar_time DATETIME NOT NULL,
  fqt INT NOT NULL DEFAULT 1,
  ma5 DECIMAL(18,8),
  ma10 DECIMAL(18,8),
  ma20 DECIMAL(18,8),
  ma60 DECIMAL(18,8),
  ema5 DECIMAL(18,8),
  ema10 DECIMAL(18,8),
  ema20 DECIMAL(18,8),
  ema60 DECIMAL(18,8),
  rsi14 DECIMAL(18,8),
  macd DECIMAL(18,8),
  macd_signal DECIMAL(18,8),
  macd_hist DECIMAL(18,8),
  boll_mid DECIMAL(18,8),
  boll_up DECIMAL(18,8),
  boll_low DECIMAL(18,8),
  kdj_k DECIMAL(18,8),
  kdj_d DECIMAL(18,8),
  kdj_j DECIMAL(18,8),
  vol_ma5 DECIMAL(18,8),
  vol_ma10 DECIMAL(18,8),
  vol_ma20 DECIMAL(18,8),
  vol_ma60 DECIMAL(18,8),
  amt_ma20 DECIMAL(18,8),
  vol_ratio20 DECIMAL(18,8),
  created_at DATETIME,
  updated_at DATETIME,
  UNIQUE KEY uk_factor (code, timeframe, bar_time, fqt),
  INDEX idx_code_tf_time (code, timeframe, bar_time)
);
